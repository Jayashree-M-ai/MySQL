-- 11. List all customers who purchased more than 1 units of any product, including the product name and total quantity purchased.
select users.user_id, users.user_name, products.product_name, 
sum(ordersdetails.quantity) as total_quantity_purchased
from users
inner join orders on users.user_id=orders.user_id 
inner join ordersdetails on  orders.order_id=ordersdetails.order_id
inner join products on ordersdetails.product_id=products.product_id
group by users.user_id, products.product_name
having sum(ordersdetails.quantity)>1;
-- 12. Find the total revenue generated by each category along with the category name
select products.category, 
sum(ordersdetails.quantity*products.price) as total_revenue
from products
inner join ordersdetails on ordersdetails.product_id=products.product_id
group by products.category;